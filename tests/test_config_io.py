class TestListBackups:

    def _do_two_saves(self, tmpdir: str) -> str:
        """Helper: write original, save once, save again. Returns config path."""
        cfg_path = os.path.join(tmpdir, "oh-my-openagent.jsonc")
        _write_file(cfg_path, ORIGINAL_JSONC)

        save(MINIMAL_CONFIG, cfg_path)

        cfg2 = dict(MINIMAL_CONFIG)
        cfg2["agents"] = {"sisyphus": {"model": "deepseek/deepseek-v4-pro"}}
        time.sleep(0.01)
        save(cfg2, cfg_path)
        return cfg_path

    def test_list_backups_returns_list(self):
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = self._do_two_saves(tmpdir)
            result = list_backups(cfg_path)
            assert isinstance(result, list)

    def test_list_backups_original_first(self):
        """original.jsonc is always the first entry when present."""
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = self._do_two_saves(tmpdir)
            result = list_backups(cfg_path)
            assert result[0].is_original is True
            assert result[0].name == "original.jsonc"

    def test_list_backups_newest_first(self):
        """Timestamped entries are listed newest-first."""
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = self._do_two_saves(tmpdir)
            result = list_backups(cfg_path)
            ts_entries = [b for b in result if not b.is_original]
            if len(ts_entries) >= 2:
                names = [b.name for b in ts_entries]
                assert names == sorted(names, reverse=True), (
                    f"Entries not newest-first: {names}"
                )

    def test_list_backups_entry_shape(self):
        """Each BackupInfo has name, path, is_original, size."""
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = self._do_two_saves(tmpdir)
            result = list_backups(cfg_path)
            for bi in result:
                assert isinstance(bi, BackupInfo)
                assert isinstance(bi.name, str)
                assert isinstance(bi.path, str)
                assert isinstance(bi.is_original, bool)
                assert isinstance(bi.size, int)

    def test_list_backups_max_10_timestamped(self):
        """list_backups returns at most 10 timestamped snapshots (+ optional original)."""
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = os.path.join(tmpdir, "oh-my-openagent.jsonc")
            _write_file(cfg_path, ORIGINAL_JSONC)
            save(MINIMAL_CONFIG, cfg_path)
            for i in range(15):
                cfg = dict(MINIMAL_CONFIG)
                cfg["agents"] = {"sisyphus": {"model": f"opencode/model-{i}"}}
                time.sleep(0.002)
                save(cfg, cfg_path)
            result = list_backups(cfg_path)
            ts_only = [b for b in result if not b.is_original]
            assert len(ts_only) <= 10

    def test_list_backups_empty_when_no_backups(self):
        """No saves yet → list_backups returns []."""
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = os.path.join(tmpdir, "oh-my-openagent.jsonc")
            result = list_backups(cfg_path)
            assert result == []


