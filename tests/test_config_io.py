class TestSerialize:

    def test_header_line(self):
        """First line is exactly the oModel header comment."""
        text = serialize(MINIMAL_CONFIG)
        assert text.startswith("// Generated by oModel — edit via `omodel`\n")

    def test_schema_forced_to_position_0(self):
        """$schema appears as the first JSON key even if it was later in the dict."""
        cfg = {"agents": {}, "categories": {}, "$schema": "https://example.com/schema"}
        text = serialize(cfg)
        body = text[text.index("\n") + 1:]
        import json
        parsed = json.loads(body)
        keys = list(parsed.keys())
        assert keys[0] == "$schema", f"$schema must be first key; got {keys}"

    def test_single_trailing_newline(self):
        """Serialized text ends with exactly one newline."""
        text = serialize(MINIMAL_CONFIG)
        assert text.endswith("\n")
        assert not text.endswith("\n\n")

    def test_no_comments_emitted(self):
        """json.dumps cannot emit comments — none should appear in output."""
        text = serialize(MINIMAL_CONFIG)
        lines = text.splitlines()
        # Only the header line starts with //
        comment_lines = [line for line in lines[1:] if line.strip().startswith("//")]
        assert comment_lines == [], f"Unexpected comment lines: {comment_lines}"

    def test_non_model_sections_preserved(self):
        """team_mode, experimental, claude_code pass through by value."""
        text = serialize(MINIMAL_CONFIG)
        import json
        body = text[text.index("\n") + 1:]
        parsed = json.loads(body)
        assert parsed["team_mode"] is False
        assert parsed["experimental"] == {"featureX": True}
        assert parsed["claude_code"] == {"enabled": True}

    def test_agents_model_preserved(self):
        """The agents.sisyphus.model value is preserved exactly."""
        text = serialize(MINIMAL_CONFIG)
        import json
        body = text[text.index("\n") + 1:]
        parsed = json.loads(body)
        assert parsed["agents"]["sisyphus"]["model"] == "opencode/claude-opus-4-7"

    def test_schema_present_when_in_cfg(self):
        text = serialize(MINIMAL_CONFIG)
        assert "$schema" in text

    def test_schema_absent_when_not_in_cfg(self):
        cfg = {"agents": {}, "categories": {}}
        text = serialize(cfg)
        assert "$schema" not in text


