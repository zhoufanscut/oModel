class TestRestore:

    def test_restore_round_trip(self):
        """Restore original.jsonc → config becomes the original content."""
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = os.path.join(tmpdir, "oh-my-openagent.jsonc")
            _write_file(cfg_path, ORIGINAL_JSONC)

            save(MINIMAL_CONFIG, cfg_path)  # rewrites config, creates original.jsonc

            # Restore original
            restore(cfg_path, "original.jsonc")

            restored = _read_file(cfg_path)
            assert restored == ORIGINAL_JSONC, "Restored content must match original verbatim"

    def test_restore_snapshots_current_first(self):
        """restore() snapshots the CURRENT file before overwriting (restore is undoable)."""
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = os.path.join(tmpdir, "oh-my-openagent.jsonc")
            _write_file(cfg_path, ORIGINAL_JSONC)

            save(MINIMAL_CONFIG, cfg_path)
            content_before_restore = _read_file(cfg_path)

            backup_dir = os.path.join(tmpdir, ".backup")
            ts_before = set(glob.glob(os.path.join(backup_dir, "[0-9]*.jsonc")))

            time.sleep(0.01)
            restore(cfg_path, "original.jsonc")

            ts_after = set(glob.glob(os.path.join(backup_dir, "[0-9]*.jsonc")))
            new_snaps = ts_after - ts_before
            assert len(new_snaps) == 1, "restore() must create exactly one new snapshot"
            # The new snapshot contains what was the live file before restore
            snap_content = _read_file(list(new_snaps)[0])
            assert snap_content == content_before_restore


