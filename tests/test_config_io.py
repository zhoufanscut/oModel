class TestSave:

    def test_first_save_creates_original_jsonc(self):
        """First save → .backup/original.jsonc created verbatim (comments intact)."""
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = os.path.join(tmpdir, "oh-my-openagent.jsonc")
            _write_file(cfg_path, ORIGINAL_JSONC)

            result = save(MINIMAL_CONFIG, cfg_path)

            assert result.changed is True
            assert result.original_created is True
            orig = os.path.join(tmpdir, ".backup", "original.jsonc")
            assert os.path.exists(orig)
            # Must be VERBATIM byte copy — comments intact
            assert _read_file(orig) == ORIGINAL_JSONC

    def test_first_save_creates_timestamped_snapshot(self):
        """First save → a timestamped .backup/<ts>.jsonc snapshot."""
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = os.path.join(tmpdir, "oh-my-openagent.jsonc")
            _write_file(cfg_path, ORIGINAL_JSONC)

            result = save(MINIMAL_CONFIG, cfg_path)

            assert result.backup is not None
            assert os.path.exists(result.backup)
            ts_name = os.path.basename(result.backup)
            # Name matches YYYYMMDD-HHMMSS.mmm.jsonc pattern
            assert re.match(r"\d{8}-\d{6}\.\d{3}\.jsonc$", ts_name), (
                f"Unexpected snapshot name: {ts_name}"
            )

    def test_second_save_adds_second_snapshot(self):
        """Second save → original.jsonc unchanged, second timestamped snapshot created."""
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = os.path.join(tmpdir, "oh-my-openagent.jsonc")
            _write_file(cfg_path, ORIGINAL_JSONC)

            save(MINIMAL_CONFIG, cfg_path)
            # Change model to trigger a second save
            cfg2 = dict(MINIMAL_CONFIG)
            cfg2["agents"] = {"sisyphus": {"model": "deepseek/deepseek-v4-pro"}}
            time.sleep(0.01)  # ensure different timestamp
            result2 = save(cfg2, cfg_path)

            backup_dir = os.path.join(tmpdir, ".backup")
            timestamped = sorted(glob.glob(os.path.join(backup_dir, "[0-9]*.jsonc")))
            assert len(timestamped) == 2, (
                f"Expected 2 timestamped snapshots, got {len(timestamped)}"
            )
            assert result2.original_created is False, "original.jsonc must not be recreated"

    def test_original_never_overwritten(self):
        """original.jsonc is created once and never overwritten on subsequent saves."""
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = os.path.join(tmpdir, "oh-my-openagent.jsonc")
            _write_file(cfg_path, ORIGINAL_JSONC)

            save(MINIMAL_CONFIG, cfg_path)
            orig_path = os.path.join(tmpdir, ".backup", "original.jsonc")
            mtime_after_first = os.path.getmtime(orig_path)

            cfg2 = dict(MINIMAL_CONFIG)
            cfg2["agents"] = {"sisyphus": {"model": "moonshotai-cn/kimi-k2.5"}}
            time.sleep(0.02)
            save(cfg2, cfg_path)

            mtime_after_second = os.path.getmtime(orig_path)
            assert mtime_after_first == mtime_after_second, "original.jsonc must not be modified"

    def test_no_change_returns_not_changed(self):
        """Saving the same content → SaveResult(changed=False), no new snapshot."""
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = os.path.join(tmpdir, "oh-my-openagent.jsonc")
            # Write what serialize would produce so there's no diff
            _write_file(cfg_path, serialize(MINIMAL_CONFIG))

            result = save(MINIMAL_CONFIG, cfg_path)

            assert result.changed is False
            assert result.backup is None

    def test_save_result_is_dataclass(self):
        """SaveResult has the fields: changed, backup, original_created."""
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = os.path.join(tmpdir, "oh-my-openagent.jsonc")
            _write_file(cfg_path, ORIGINAL_JSONC)

            result = save(MINIMAL_CONFIG, cfg_path)

            assert hasattr(result, "changed")
            assert hasattr(result, "backup")
            assert hasattr(result, "original_created")

    def test_atomic_write(self):
        """The config file is updated atomically (temp+rename)."""
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = os.path.join(tmpdir, "oh-my-openagent.jsonc")
            _write_file(cfg_path, ORIGINAL_JSONC)

            save(MINIMAL_CONFIG, cfg_path)

            # After save, config must be parseable and match serialized form
            written = _read_file(cfg_path)
            assert written == serialize(MINIMAL_CONFIG)

    def test_clean_rewrite_drops_comments(self):
        """First save produces a comment-free file (comment loss is EXPECTED, decision #13)."""
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = os.path.join(tmpdir, "oh-my-openagent.jsonc")
            _write_file(cfg_path, ORIGINAL_JSONC)

            save(MINIMAL_CONFIG, cfg_path)

            written = _read_file(cfg_path)
            # No LINES starting with '//' (after stripping whitespace) except the oModel header.
            # (Note: '//' inside a JSON string value like a URL is fine and expected.)
            lines = written.splitlines()
            comment_lines = [line for line in lines[1:] if line.strip().startswith("//")]
            assert comment_lines == [], (
                "Comments inside the config body must be dropped on clean rewrite: "
                + str(comment_lines)
            )

    def test_comment_loss_asserted_as_expected(self):
        """Explicitly assert the original JSONC had comments; the saved version does not.
        (Decision #13: lossy by design. The original is preserved in .backup/original.jsonc.)"""
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = os.path.join(tmpdir, "oh-my-openagent.jsonc")
            _write_file(cfg_path, ORIGINAL_JSONC)

            assert "//" in ORIGINAL_JSONC, "Test fixture must have comments"

            save(MINIMAL_CONFIG, cfg_path)
            written = _read_file(cfg_path)
            # Comments gone in the new file (only header allowed)
            body_lines = written.split("\n")[1:]
            comment_lines = [line for line in body_lines if line.strip().startswith("//")]
            assert comment_lines == [], f"Palette comments must be gone after save: {comment_lines}"

            # But original is verbatim
            orig_path = os.path.join(tmpdir, ".backup", "original.jsonc")
            assert _read_file(orig_path) == ORIGINAL_JSONC


