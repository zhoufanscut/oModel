class TestPruneSnapshots:

    def test_prune_keeps_newest_20(self):
        """After >20 saves, only the newest 20 timestamped snapshots remain."""
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = os.path.join(tmpdir, "oh-my-openagent.jsonc")
            _write_file(cfg_path, ORIGINAL_JSONC)

            # First save (creates original.jsonc)
            save(MINIMAL_CONFIG, cfg_path)

            # 21 more saves with distinct content
            for i in range(21):
                cfg = dict(MINIMAL_CONFIG)
                cfg["agents"] = {"sisyphus": {"model": f"opencode/model-{i}"}}
                time.sleep(0.002)  # force distinct timestamps
                save(cfg, cfg_path)

            backup_dir = os.path.join(tmpdir, ".backup")
            timestamped = glob.glob(os.path.join(backup_dir, "[0-9]*.jsonc"))
            assert len(timestamped) <= 20, (
                f"Expected ≤20 timestamped snapshots, got {len(timestamped)}"
            )

    def test_prune_never_removes_original(self):
        """original.jsonc is NEVER pruned regardless of snapshot count."""
        with tempfile.TemporaryDirectory() as tmpdir:
            cfg_path = os.path.join(tmpdir, "oh-my-openagent.jsonc")
            _write_file(cfg_path, ORIGINAL_JSONC)

            save(MINIMAL_CONFIG, cfg_path)
            for i in range(25):
                cfg = dict(MINIMAL_CONFIG)
                cfg["agents"] = {"sisyphus": {"model": f"opencode/model-{i}"}}
                time.sleep(0.002)
                save(cfg, cfg_path)

            orig_path = os.path.join(tmpdir, ".backup", "original.jsonc")
            assert os.path.exists(orig_path), "original.jsonc must never be pruned"


